def seed():
    """Laedt bestehenden Cache und ergaenzt fehlende Eintraege."""
    cache: dict = {}
    if CACHE_PATH.exists():
        raw = CACHE_PATH.read_text(encoding="utf-8")
        cache = json.loads(raw)

    added = 0
    for key, (lat, lon, display) in CITIES.items():
        if key not in cache:
            cache[key] = {"lat": lat, "lon": lon, "display_name": display}
            added += 1

    CACHE_PATH.write_text(
        json.dumps(cache, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(f"Geocode-Cache: {added} neue Eintraege, {len(cache)} total")


