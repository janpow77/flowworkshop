CONTENT = """
Zuwendungsbescheid

Aktenzeichen: ESF-HE-2024-1203
Datum: 22. Mai 2024
Zuwendungsempfänger: Bildungswerk der Hessischen Wirtschaft e.V., Frankfurt am Main

Gegenstand der Förderung:
Qualifizierungsmaßnahme für Langzeitarbeitslose im Rahmen des ESF+-Programms Hessen 2021-2027, Priorität 1 „Förderung der Beschäftigung".

Bewilligungszeitraum: 01.07.2024 bis 30.06.2026
Teilnehmerzielzahl: 120 Personen (davon mindestens 50 % Frauen)

Fördersumme: 620.000,00 EUR (ESF+-Mittel: 496.000,00 EUR, Landesmittel: 124.000,00 EUR)
Gesamtkosten: 775.000,00 EUR
Förderquote: 80 %

Kostenstruktur (vereinfachte Kostenoptionen gemäß Art. 53-56 VO (EU) 2021/1060):
- Direkte Personalkosten: Abrechnung nach Einheitskosten (Stundensatz 45,00 EUR gemäß Leitfaden)
- Restkosten: Pauschalfinanzierung in Höhe von 40 % der direkten Personalkosten (Art. 56 Abs. 1)
- Keine gesonderte Belegprüfung für die Restkostenpauschale erforderlich

Auflagen und Bedingungen:

1. Die Teilnehmerdaten sind gemäß Anhang I der VO (EU) 2021/1057 (ESF+-Verordnung) zu erfassen. Für jeden Teilnehmer sind die gemeinsamen Output- und Ergebnisindikatoren bei Ein- und Austritt vollständig zu dokumentieren.
2. Die datenschutzrechtliche Einwilligung jedes Teilnehmers ist vor Maßnahmenbeginn einzuholen und zu archivieren (Art. 6 Abs. 1 lit. a DSGVO).
3. Die Teilnehmer sind bei Eintritt nach dem „Erwerbsstatus", „Alter", „Geschlecht", „Bildungsstand" und ggf. „Migrationshintergrund" zu klassifizieren.
4. Bei Austritt sind die Ergebnisindikatoren zu erheben: Beschäftigungsstatus, erworbene Qualifikation, Teilnahme an weiterführenden Maßnahmen.
5. Der Einheitskostensatz von 45,00 EUR/Stunde darf nur für tatsächlich geleistete und dokumentierte Unterrichtsstunden abgerechnet werden. Stundennachweise sind pro Dozent zu führen.
6. Die Restkostenpauschale von 40 % wird automatisch auf Basis der anerkannten direkten Personalkosten berechnet. Eine gesonderte Belegvorlage ist nicht erforderlich (Art. 56 Abs. 1 VO (EU) 2021/1060).
7. Die Publizitätspflichten gemäß Art. 50 VO (EU) 2021/1060 sind einzuhalten. Das ESF+-Logo ist auf allen Schulungsunterlagen, Teilnahmebescheinigungen und am Schulungsort sichtbar anzubringen.
8. Der Verwendungsnachweis ist spätestens drei Monate nach Ende des Bewilligungszeitraums vorzulegen und muss den zahlenmäßigen Nachweis, den Sachbericht sowie die vollständigen Teilnehmerstatistiken enthalten.
9. Änderungen der Maßnahmenkonzeption oder der Teilnehmerzielzahl bedürfen der vorherigen schriftlichen Genehmigung.
10. Der Zuwendungsempfänger hat die Gleichstellung der Geschlechter als Querschnittsziel aktiv zu berücksichtigen und in der Berichterstattung darzulegen.

Rechtsgrundlagen: VO (EU) 2021/1060 (Dachverordnung), VO (EU) 2021/1057 (ESF+-Verordnung), Hessisches ESF+-Programm 2021-2027.

gez. Ministerialrätin Dr. Weber
Hessisches Ministerium für Soziales und Integration
"""
